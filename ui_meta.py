def apply_params(values: dict) -> dict:
    """display-значения ползунков -> config (с учётом scale)."""
    cfg = {}
    for grp in PARAM_GROUPS:
        for p in grp["params"]:
            if p["key"] not in values:
                continue
            v = values[p["key"]]
            if p.get("type") == "select":
                cfg[p["key"]] = int(v)
            else:
                scaled = v * p.get("scale", 1)
                cfg[p["key"]] = scaled
    return cfg


